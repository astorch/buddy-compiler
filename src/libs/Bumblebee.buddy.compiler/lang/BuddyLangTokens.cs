namespace Bumblebee.buddy.compiler.lang {
    /// <summary>
    /// Defines special buddy language terminal tokens.
    /// </summary>
    public static class BuddyLangTokens {
        // ReSharper disable InconsistentNaming

        /// <summary> Quote </summary>
        public const char QUOTE = '"';

        /// <summary> Left arrow bracket </summary>
        public const char LAB = '<';

        /// <summary> Right arrow bracket </summary>
        public const char RAB = '>';

        /// <summary> Left square bracket </summary>
        public const char LSB = '[';

        /// <summary> Right square bracket </summary>
        public const char RSB = ']';

        /// <summary>Left curly bracket</summary>
        public const char LCB = '{';

        /// <summary>Right curly bracket</summary>
        public const char RCB = '}';

        /// <summary> Left round bracket </summary>
        public const char LRB = '(';

        /// <summary> Right round bracket </summary>
        public const char RRB = ')';

        /// <summary>Whitespace</summary>
        public const char WS = ' ';

        /// <summary> Dot </summary>
        public const char DOT = '.';

        // ReSharper restore InconsistentNaming
    }
}